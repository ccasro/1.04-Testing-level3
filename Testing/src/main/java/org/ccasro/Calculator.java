package org.ccasro;

public class Calculator {

    public double div(double v, double v1) {
        return v / v1;
    }

    public double add(double v, double v1) {
        return v + v1;
    }

    public double sub(double v, double v1) {
        return v - v1;
    }

    public double mul(double v, double v1) {
        return v * v1;
    }

}
