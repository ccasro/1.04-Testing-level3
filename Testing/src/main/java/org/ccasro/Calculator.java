package org.ccasro;

public class Calculator {
    public int add(int i, int i1) {
        return i + i1;
    }


    public int sub(int i, int i1) {
        return i - i1;
    }


    public double div(double v, double v1) {
        return v / v1;
    }


    public int mul(int i, int i1) {
        return i * i1;
    }
}
